func createImages() {
	iup.Image(22, 22, pixmap_start).SetHandle("img_start").SetAttributes(`1="0 0 0", 2="BGCOLOR"`)
	iup.Image(22, 22, pixmap_play).SetHandle("img_play").SetAttributes(`1="0 0 0", 2="BGCOLOR"`)
	iup.Image(22, 22, pixmap_forward).SetHandle("img_forward").SetAttributes(`1="0 0 0", 2="BGCOLOR"`)
	iup.Image(22, 22, pixmap_rewind).SetHandle("img_rewind").SetAttributes(`1="0 0 0", 2="BGCOLOR"`)
	iup.Image(22, 22, pixmap_show).SetHandle("img_show").SetAttributes(`1="0 0 0", 2="BGCOLOR"`)
}

func main() {
	iup.Open()
	iupcontrols.Open()
	defer iup.Close()

	var speed float64 = 0.00001

	createImages()

	gauge := iupcontrols.Gauge().SetAttributes(map[string]string{
		"EXPAND": "YES",
		/*"MAX": "3.5",
		"MIN": "0.7",*/
	})

	idle_action := func() int {
		value := gauge.GetDouble("VALUE")
		value += speed
		if value > gauge.GetDouble("MAX") {
			value = gauge.GetDouble("MIN")
		}
		gauge.SetAttribute("VALUE", value)

		return iup.DEFAULT
	}

	btn_start := iup.Button("start").SetAttributes(`IMAGE="img_start", TIP="Start"`).SetCallback("ACTION", func(ih iup.Ihandle) int {
		gauge.SetAttribute("VALUE", gauge.GetDouble("MIN"))
		return iup.DEFAULT
	})

	btn_pause := iup.Button("pause").SetAttributes(`IMAGE="img_play", TIP="Pause"`).SetCallback("ACTION", func(ih iup.Ihandle) int {
		if iup.GetFunction("IDLE_ACTION") == 0 {
			iup.SetFunction("IDLE_ACTION", idle_action)
		} else {
			iup.SetFunction("IDLE_ACTION", nil)
		}
		return iup.DEFAULT
	})

	btn_accelerate := iup.Button("accelerate").SetAttributes(`IMAGE="img_forward", TIP="Accelerate"`).SetCallback("ACTION", func(ih iup.Ihandle) int {
		speed *= 2
		if speed > 1.0 {
			speed = 1.0
		}
		return iup.DEFAULT
	})

	btn_decelerate := iup.Button("decelerate").SetAttributes(`IMAGE="img_rewind", TIP="Decelerate"`).SetCallback("ACTION", func(ih iup.Ihandle) int {
		speed /= 2
		return iup.DEFAULT
	})

	btn_show := iup.Button("show").SetAttributes(`IMAGE="img_show", TIP="Show"`).SetCallback("ACTION", func(ih iup.Ihandle) int {
		if gauge.GetInt("SHOWTEXT") == 0 {
			gauge.SetAttributes(`SHOWTEXT=YES, DASHED=NO`)
		} else {
			gauge.SetAttributes(`SHOWTEXT=NO, DASHED=YES`)
		}
		return iup.DEFAULT
	})

	hbox := iup.Hbox(
		iup.Fill(), btn_pause, btn_start, btn_decelerate, btn_accelerate, btn_show, iup.Fill(),
	)

	dlg := iup.Dialog(
		iup.Vbox(gauge, hbox).SetAttributes(`MARGIN=10x10, GAP=10`),
	).SetAttributes(`TITLE="IupGauge Example"`)

	iup.SetFunction("IDLE_ACTION", idle_action)

	iup.Show(dlg)
	iup.MainLoop()
}
